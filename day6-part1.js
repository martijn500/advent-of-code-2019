let totalDistance = 0;

const orbitorsSorted = planets.map(planet => {
    const planetName = planet.name;
    const orbitors = [];
    planets.forEach(plant => {
        if (plant.name === planetName) {
            orbitors.push(plant.orbitor);
        }
    });
    return {
        name: planetName,
        orbitors: orbitors
    };
});

for (let planet of orbitorsSorted) {
    if (!uniquePlanetsMap.has(planet.name)) {
        uniquePlanetsMap.set(planet.name, true);
        uniquePlanets.push({
            name: planet.name,
            orbitors: planet.orbitors
        });
    }
}

const calculatePlanetOrbitorsDistance = (planet) => {
    // { name: 'COM', orbitors: ['K4F', '4RG'], parentDistance: 0, distanceFromParent: 0 }
    planet.orbitors.forEach(orbitor => {
        // 'K4F'
        const orbitorAsPlanet = uniquePlanets.find(planeto => planeto.name === orbitor) || { name: orbitor };
        // { name: 'K4F', orbitors: ['3RR']}
        orbitorAsPlanet.parentDistance = planet.distanceFromParent;
        // { name: 'K4F', orbitors: ['3RR'], parentDistance: 0 }
        orbitorAsPlanet.distanceFromParent = orbitorAsPlanet.parentDistance + 1;
        // { name: 'K4F', orbitors: ['3RR'], parentDistance: 0, distanceFromPlanet: 1}

        totalDistance += orbitorAsPlanet.distanceFromParent;

        if (orbitorAsPlanet.hasOwnProperty('orbitors')) {
            calculatePlanetOrbitorsDistance(orbitorAsPlanet);
        }
    });
}

